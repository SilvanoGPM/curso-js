console.log("Tipos de Função");

// Função que somete executa um trecho de código, sem retorno.
function fazerSaudacao(nome) {
    console.log("Olá, " + nome + "!");
}

// Função que executa um trecho de código e retorna um valor.
function somar(n, m) {
    return n + m;
}

fazerSaudacao("Silvano");
console.log(somar(10, 10));

